alphabet = "abcdefghijklmnopqrstuvwxyz"

def get_letter_at_position(position):
    return alphabet[position]

# Letter at  0: a
print("Letter at  0: ", get_letter_at_position(0))

# Letter at  1: b
print("Letter at  1: ", get_letter_at_position(1))

# Letter at  18: s
print("Letter at 18: ", get_letter_at_position(18))

# Letter at  27: ?
print("Letter at 27: ", get_letter_at_position(27))
    
